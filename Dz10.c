int Task10()
{
	int  a, b, c;
	printf("\nВведите длины стороны прямоугольного треугольника (Сторона с должна быть наибольшей):\n");
	printf("Сторона a\n");
	scanf("%d", &a);
	printf("Сторона b\n");
	scanf("%d", &b);
	printf("Сторона c\n");
	scanf("%d", &c);
	if (c * c == a * a + b * b)
	{
		Treugolnik(a, b, c); 
	}
	else
	{
		printf("\nТакого треугольника не существует!");
	}
}



int Treugolnik( a, b, c)
{
	int chose;
	printf("\nВыбирете желаемое действие\n1 - Нахождение площади фигуры\n2 - Выведение определения фигуры\n3 - Рисование фигуры\n");
	scanf("%d", &chose);
	switch (chose)
	{
	case 1:
		Tri_area(a, b, c);
		break;

	case 2:
		printf("\nТреугольник");
		break;

	case 3:
		Tri_paint(a, b, c);
		break;

	default:
		printf("\nНеверное значение!");
		break;
	}

}


int Tri_area( a, b, c)
{
	double p;
	p = ((a + b + c) / 2);
	printf("\nПлощадь треугольника равна: %.2f\n", sqrt( p * (p-a) * (p-b) * (p-c) ));
}




int Tri_paint(a, b, c)
{
	printf("Вот рисунок треугольника:\n");
	for (int i = 0; i < a; i++)
	{ 
		for (int j = 0; j <= i; j++)
		{ 
			printf("*");
		}
		printf("\n");
	}
}